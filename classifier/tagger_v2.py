def _normalize(text: str) -> str:
    return re.sub(r"\s+", " ", (text or "").replace("ё", "е").lower()).strip()


def _match(text: str, pattern) -> bool:
    if isinstance(pattern, re.Pattern):
        return pattern.search(text) is not None
    normalized_pattern = _normalize(str(pattern))
    if normalized_pattern in EXACT_WORD_PATTERNS:
        return _contains_exact_word(text, normalized_pattern)
    if normalized_pattern in WORD_STEM_PATTERNS:
        return _contains_word_stem(text, normalized_pattern)
    return pattern in text


EXACT_WORD_PATTERNS = {
    "бпла", "ввп", "всу", "вуз", "егэ", "елс", "ес", "гру", "жкх", "мвд",
    "нато", "оон", "огэ", "пво", "ржд", "ркн", "сво", "сми", "смсп", "трк",
    "ук рф", "фсб", "цик", "цб",
}

WORD_STEM_PATTERNS = {
    "иск", "лес", "мэр", "суд",
}

PARLIAMENT_CONTEXT_PATTERNS = [
    "госдум", "депутат", "фракц", "комитет", "законопроект",
    "совет федерации", "сенатор", "председатель гд",
]


def _contains_exact_word(text: str, word: str) -> bool:
    return re.search(rf"(?<![а-яёa-z]){re.escape(word)}(?![а-яёa-z])", text) is not None


def _contains_word_stem(text: str, stem: str) -> bool:
    return re.search(rf"(?<![а-яёa-z]){re.escape(stem)}[а-яёa-z-]*(?![а-яёa-z])", text) is not None


def _extract_trigger_context(text: str, pattern_str: str, window: int = 60) -> str:
    norm = _normalize(text)
    pat_norm = _normalize(pattern_str)
    idx = norm.find(pat_norm)
    if idx < 0:
        try:
            m = re.search(pattern_str, norm)
            if m:
                idx = m.start()
        except Exception:
            pass
    if idx < 0:
        return pattern_str[:40]
    start = max(0, idx - window // 2)
    end = min(len(text), idx + len(pat_norm) + window // 2)
    return text[start:end].strip()[:120]


def _score_rules(text: str, rules: List[Tuple], add_context_tags: bool = False) -> Dict[str, float]:
    norm = f" {_normalize(text)} "
    scores = {}
    explanations = {}
    for tag, patterns, threshold in rules:
        tag_score = 0.0
        matched = False
        tag_expls = []
        for pattern, points in patterns:
            if _match(norm, pattern):
                matched = True
                tag_score += points
                pat_str = pattern.pattern if isinstance(pattern, re.Pattern) else str(pattern)
                context = _extract_trigger_context(text, pat_str)
                tag_expls.append({
                    "pattern": pat_str,
                    "points": points,
                    "trigger": context,
                })
        if matched and tag_score >= threshold:
            scores[tag] = tag_score
            explanations[tag] = tag_expls
    if add_context_tags:
        has_parliament_context = any(pattern in norm for pattern in PARLIAMENT_CONTEXT_PATTERNS)
        if has_parliament_context:
            for surname in DEPUTY_SURNAMES:
                if _contains_word_stem(norm, surname):
                    scores.setdefault("duma", 0)
                    scores["duma"] = scores.get("duma", 0) + 3.0
                    explanations.setdefault("duma", [])
                    explanations["duma"].append({
                        "pattern": f"deputy_surname:{surname}",
                        "points": 3.0,
                        "trigger": surname,
                    })
        for region in RUSSIAN_REGIONS:
            if _contains_word_stem(norm, region):
                scores.setdefault("regional", 0)
                scores["regional"] = scores.get("regional", 0) + 1.0
                explanations.setdefault("regional", [])
                explanations["regional"].append({
                    "pattern": f"region:{region}",
                    "points": 1.0,
                    "trigger": region,
                })
    return scores, explanations


def infer_tags_v2(text: str, max_per_level: int = 4) -> Dict[int, List[Tuple[str, float]]]:
    result = {1: [], 2: [], 3: []}
    if not text or not text.strip():
        return result
    for level, rules, max_tags in [
        (1, LEVEL1_RULES, max_per_level),
        (2, LEVEL2_RULES, max_per_level),
        (3, LEVEL3_RULES, max_per_level),
    ]:
        scores, _ = _score_rules(text, rules, add_context_tags=(level == 2))
        ordered = sorted(scores.items(), key=lambda x: (-x[1], x[0]))
        result[level] = ordered[:max_tags]
    return result


def infer_tags_v2_with_explanations(text: str, max_per_level: int = 4) -> Tuple[Dict[int, List[Tuple[str, float]]], Dict[int, List[Dict]]]:
    result = {1: [], 2: [], 3: []}
    all_explanations = {1: [], 2: [], 3: []}
    if not text or not text.strip():
        return result, all_explanations
    for level, rules, max_tags in [
        (1, LEVEL1_RULES, max_per_level),
        (2, LEVEL2_RULES, max_per_level),
        (3, LEVEL3_RULES, max_per_level),
    ]:
        scores, explanations = _score_rules(text, rules, add_context_tags=(level == 2))
        ordered = sorted(scores.items(), key=lambda x: (-x[1], x[0]))
        result[level] = ordered[:max_tags]
        for tag_name, _ in result[level]:
            if tag_name in explanations:
                for expl in explanations[tag_name]:
                    all_explanations[level].append({
                        "tag_name": tag_name,
                        "tag_level": level,
                        **expl,
                    })
    return result, all_explanations


def tag_content_items(settings: dict = None):
    if settings is None:
        settings = load_settings()
    conn = get_db(settings)
    rows = conn.execute(
        """
        SELECT id, title, body_text
        FROM content_items
        WHERE id NOT IN (SELECT DISTINCT content_item_id FROM content_tags WHERE tag_level >= 1)
        LIMIT 5000
        """
    ).fetchall()
    if not rows:
        log.info("No untagged content items")
        conn.close()
        return
    tagged = 0
    for row in rows:
        content_id = row["id"]
        text = f"{row['title'] or ''}\n{row['body_text'] or ''}"
        tags, explanations = infer_tags_v2_with_explanations(text)
        for level, tag_list in tags.items():
            for tag_name, score in tag_list:
                try:
                    conn.execute(
                        "INSERT OR IGNORE INTO content_tags(content_item_id, tag_level, tag_name, confidence, tag_source) VALUES(?,?,?,?, 'rule')",
                        (content_id, level, tag_name, min(score / 20.0, 1.0)),
                    )
                    tag_row = conn.execute(
                        "SELECT id FROM content_tags WHERE content_item_id=? AND tag_level=? AND tag_name=?",
                        (content_id, level, tag_name),
                    ).fetchone()
                    if tag_row:
                        tag_id = tag_row[0]
                        level_expls = [e for e in explanations.get(level, []) if e.get("tag_name") == tag_name]
                        for expl in level_expls[:3]:
                            try:
                                conn.execute(
                                    "INSERT INTO tag_explanations(content_tag_id, trigger_text, trigger_rule, matched_pattern, confidence_raw) VALUES(?,?,?,?,?)",
                                    (tag_id, expl.get("trigger", "")[:300], tag_name, expl.get("pattern", ""), expl.get("points", 0)),
                                )
                            except Exception:
                                pass
                except Exception:
                    pass
        tagged += 1
    conn.commit()
    conn.close()
    log.info("Tagged %d content items", tagged)


def main():
    logging.basicConfig(level=logging.INFO, format="%(asctime)s [%(levelname)s] %(message)s")
    tag_content_items()


if __name__ == "__main__":
    main()
