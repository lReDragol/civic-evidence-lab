def _normalize(text: str) -> str:
    return re.sub(r"\s+", " ", (text or "").replace("ё", "е").lower()).strip()


def _contains_exact_word(text: str, word: str) -> bool:
    return re.search(rf"(?<![а-яёa-z]){re.escape(word)}(?![а-яёa-z])", text) is not None


def _contains_word_stem(text: str, stem: str) -> bool:
    return re.search(rf"(?<![а-яёa-z]){re.escape(stem)}[а-яёa-z-]*(?![а-яёa-z])", text) is not None


def _pattern_matches(text: str, pattern: str) -> bool:
    normalized = _normalize(pattern)
    if not normalized:
        return False
    if " " in normalized or any(ch.isdigit() for ch in normalized):
        return normalized in text
    if len(normalized) <= 2:
        return _contains_exact_word(text, normalized)
    if len(normalized) <= 4:
        return _contains_word_stem(text, normalized)
    return normalized in text


TAG_REGISTRY = {
    tag_name: [_normalize(p) for p in patterns if _normalize(p)]
    for tag_name, patterns in KEYWORD_TAGS.items()
}

REGION_TAG_NAMES = [f"регион:{r}" for r in RUSSIAN_REGIONS]
DEPUTY_TAG_NAMES = [f"депутат:{s}" for s in DEPUTY_SURNAMES]


def infer_granular_tags(text: str, max_keyword: int = 10, max_region: int = 3, max_deputy: int = 5) -> Dict[str, List[str]]:
    result = {"keyword": [], "region": [], "deputy": []}
    if not text or not text.strip():
        return result

    norm = f" {_normalize(text)} "

    for tag_name, patterns in TAG_REGISTRY.items():
        for pattern in patterns:
            if _pattern_matches(norm, pattern):
                result["keyword"].append(tag_name)
                break
        if len(result["keyword"]) >= max_keyword:
            break

    for i, region in enumerate(RUSSIAN_REGIONS):
        region_norm = _normalize(region)
        if _contains_word_stem(norm, region_norm):
            result["region"].append(f"регион:{region}")
        if len(result["region"]) >= max_region:
            break

    for surname in DEPUTY_SURNAMES:
        if _contains_word_stem(norm, surname):
            result["deputy"].append(f"депутат:{surname}")
        if len(result["deputy"]) >= max_deputy:
            break

    return result


def tag_content_granular(settings: dict = None, batch_size: int = 5000):
    if settings is None:
        settings = load_settings()

    conn = get_db(settings)

    rows = conn.execute(
        """
        SELECT id, title, body_text
        FROM content_items
        WHERE granular_processed = 0
          AND (length(body_text) > 5 OR length(title) > 3)
        LIMIT ?
        """,
        (batch_size,),
    ).fetchall()

    if not rows:
        log.info("No untagged content items for granular tagging")
        conn.close()
        return

    deputy_rows = conn.execute(
        "SELECT dp.entity_id, dp.full_name, dp.faction, e.canonical_name "
        "FROM deputy_profiles dp JOIN entities e ON e.id = dp.entity_id WHERE dp.is_active=1"
    ).fetchall()
    deputy_db = {}
    for dr in deputy_rows:
        entity_id, full_name, faction, canonical = dr
        parts = full_name.split() if full_name else canonical.split()
        surname = parts[0].lower() if parts else ""
        if surname:
            deputy_db.setdefault(surname, []).append({
                "entity_id": entity_id,
                "full_name": full_name or canonical,
                "faction": faction or "",
                "short": f"{parts[0]} {parts[1][:1]}." if len(parts) >= 2 else parts[0],
            })

    log.info("Granular tagging %d items (deputy DB: %d surnames)", len(rows), len(deputy_db))

    PARLIAMENT_CONTEXT = ["депутат", "госдум", "фракц", "комитет", "законопроект",
                          "совет федерации", "сенатор", "голосован", "дума"]

    tagged = 0
    total_tags = 0
    for row in rows:
        content_id = row["id"]
        text = f"{row['title'] or ''}\n{row['body_text'] or ''}"
        if not text.strip():
            conn.execute("UPDATE content_items SET granular_processed=1 WHERE id=?", (content_id,))
            continue

        tags = infer_granular_tags(text)

        for tag_name in tags["keyword"]:
            try:
                conn.execute(
                    "INSERT OR IGNORE INTO content_tags(content_item_id, tag_level, tag_name, confidence, tag_source) VALUES(?,0,?,0.8,'granular')",
                    (content_id, tag_name),
                )
                total_tags += 1
            except Exception:
                pass

        for tag_name in tags["region"]:
            try:
                conn.execute(
                    "INSERT OR IGNORE INTO content_tags(content_item_id, tag_level, tag_name, confidence, tag_source) VALUES(?,0,?,0.9,'granular')",
                    (content_id, tag_name),
                )
                total_tags += 1
            except Exception:
                pass

        norm_text = _normalize(text)
        has_parliament_context = any(ctx in norm_text for ctx in PARLIAMENT_CONTEXT)

        matched_deputies = set()
        for surname in DEPUTY_SURNAMES:
            if surname in norm_text:
                matched_deputies.add(surname)

        for surname in matched_deputies:
            if surname in deputy_db:
                for dep_info in deputy_db[surname]:
                    tag_name = f"депутат:{dep_info['short']}"
                    confidence = 0.9 if has_parliament_context else 0.3

                    try:
                        conn.execute(
                            "INSERT OR IGNORE INTO content_tags(content_item_id, tag_level, tag_name, confidence, tag_source) VALUES(?,0,?,?,'granular')",
                            (content_id, tag_name, confidence),
                        )
                        total_tags += 1
                    except Exception:
                        pass

                    if has_parliament_context:
                        try:
                            conn.execute(
                                "INSERT OR IGNORE INTO entity_mentions(entity_id, content_item_id, mention_type, confidence) VALUES(?,?,'deputy_tag',?)",
                                (dep_info["entity_id"], content_id, confidence),
                            )
                        except Exception:
                            pass

                    if not has_parliament_context:
                        try:
                            conn.execute(
                                "INSERT OR IGNORE INTO content_tags(content_item_id, tag_level, tag_name, confidence, tag_source) VALUES(?,3,'needs_review',0.5,'granular_deputy')",
                                (content_id,),
                            )
                        except Exception:
                            pass
            else:
                tag_name = f"депутат:{surname}"
                confidence = 0.85 if has_parliament_context else 0.2
                try:
                    conn.execute(
                        "INSERT OR IGNORE INTO content_tags(content_item_id, tag_level, tag_name, confidence, tag_source) VALUES(?,0,?,?,'granular')",
                        (content_id, tag_name, confidence),
                    )
                    total_tags += 1
                except Exception:
                    pass

        tagged += 1

        conn.execute("UPDATE content_items SET granular_processed=1 WHERE id=?", (content_id,))

    conn.commit()
    conn.close()
    log.info("Granular tagged %d items with %d tags", tagged, total_tags)


def main():
    logging.basicConfig(level=logging.INFO, format="%(asctime)s [%(levelname)s] %(message)s")
    tag_content_granular()


if __name__ == "__main__":
    main()
